<h2>NASOALVEOLAR MOLDING AT BIRTH </h2>
<ol style="color: white;">
<li>Pre-surgical infant orthopedics is sometimes used to reposition the segments of the cleft maxilla prior to lip repair. This can vary in complexity from lip taping to narrow the cleft, to a bonnet with elastic to ventroflex a protruding premaxilla, to more complex Nasoalveolar Molding appliances.</li>
<li>These appliances can make lip closure easier. While this short-term benefit is clear, long term effects are unclear and controversial.</li>
