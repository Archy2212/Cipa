The mission of CIPA is to enhance the quality of life for individuals affected by cleft lip and palate in India.<br>

<h2><strong>K</strong>ey <span>Function:</span></h2>
Providing comprehensive information to educate cleft patients and their families ;
Making referrals to cleft/craniofacial treatment teams;
Organize local parent-to-parent support through its nation-wide network
Represent the interests of patients and parents, influencing policy on future treatment of cleft lip and palate
Raise funds  for informative literature and services
